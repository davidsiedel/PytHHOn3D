from pythhon3d.core.basis import Basis
import numpy as np
import pytest

"""
==========================================================================================
TESTING THE BASIS POWER MATRIX
==========================================================================================
"""

# ----------------------------------------------------------------------------------------
# DIMENSION 0
# ----------------------------------------------------------------------------------------
expected_k1_d0 = np.array([[0]])
expected_k2_d0 = np.array([[0]])
expected_k3_d0 = np.array([[0]])

# ----------------------------------------------------------------------------------------
# DIMENSION 1
# ----------------------------------------------------------------------------------------
expected_k1_d1 = np.array([[0], [1]])
expected_k2_d1 = np.array([[0], [1], [2]])
expected_k3_d1 = np.array([[0], [1], [2], [3]])

# ----------------------------------------------------------------------------------------
# DIMENSION 2
# ----------------------------------------------------------------------------------------
expected_k1_d2 = np.array([[0, 0], [0, 1], [1, 0]])
expected_k2_d2 = np.array([[0, 0], [0, 1], [1, 0], [0, 2], [1, 1], [2, 0]])
expected_k3_d2 = np.array(
    [[0, 0], [0, 1], [1, 0], [0, 2], [1, 1], [2, 0], [0, 3], [1, 2], [2, 1], [3, 0]]
)

# ----------------------------------------------------------------------------------------
# DIMENSION 3
# ----------------------------------------------------------------------------------------
expected_k1_d3 = np.array([[0, 0, 0], [0, 0, 1], [0, 1, 0], [1, 0, 0]])
expected_k2_d3 = np.array(
    [
        [0, 0, 0],
        [0, 0, 1],
        [0, 1, 0],
        [1, 0, 0],
        [0, 0, 2],
        [0, 1, 1],
        [0, 2, 0],
        [1, 0, 1],
        [1, 1, 0],
        [2, 0, 0],
    ]
)
expected_k3_d3 = np.array(
    [
        [0, 0, 0],
        [0, 0, 1],
        [0, 1, 0],
        [1, 0, 0],
        [0, 0, 2],
        [0, 1, 1],
        [0, 2, 0],
        [1, 0, 1],
        [1, 1, 0],
        [2, 0, 0],
        [0, 0, 3],
        [0, 1, 2],
        [0, 2, 1],
        [0, 3, 0],
        [1, 0, 2],
        [1, 1, 1],
        [1, 2, 0],
        [2, 0, 1],
        [2, 1, 0],
        [3, 0, 0],
    ]
)

test_data_k_d = [
    (1, 0, expected_k1_d0),
    (2, 0, expected_k2_d0),
    (3, 0, expected_k3_d0),
    (1, 1, expected_k1_d1),
    (2, 1, expected_k2_d1),
    (3, 1, expected_k3_d1),
    (1, 2, expected_k1_d2),
    (2, 2, expected_k2_d2),
    (3, 2, expected_k3_d2),
    (1, 3, expected_k1_d3),
    (2, 3, expected_k2_d3),
    (3, 3, expected_k3_d3),
]


@pytest.mark.parametrize("k,d, expected", test_data_k_d)
def test_basis_pow_matrix(k, d, expected):
    b = Basis(k, d)
    assert (b.pow_matrix == expected).all()


